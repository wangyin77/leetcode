/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
class Solution {
public:
    ListNode *detectCycle(ListNode *head) {
        if(!head) return NULL;
        ListNode *fast = head, *low = head;
        do{
            fast = fast->next;
            if(!fast) return NULL;
            fast = fast->next;
            if(!fast) return NULL;
            low = low->next;
        }while(fast != low);//用do..while可以少写点判断
        low = head;
        while(fast != low){
            fast = fast->next;
            low = low->next;
        }
        return fast;
    }
};
