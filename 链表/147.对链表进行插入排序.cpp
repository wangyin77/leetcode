/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
class Solution {
public:
    ListNode* insertionSortList(ListNode* head) {// 这题比较难的
        ListNode *dummy = new ListNode(-1);
        while(head){//用head循环可以少定义一个指针
            ListNode *next = head->next, *t = dummy;
            while(t->next && t->next->val <= head->val) t = t->next;
            //一定要循环到结尾，不能只到head
            head->next = t->next, t->next = head;
            //head总有地方放，head是最大的时候，t到了链表结尾，head指空
            head = next;
        }
        return dummy->next;        
    }
};
